void Game::run()
{	
	int line = 0;
	float terrainDown = 0;
	float terrainSpeed = 1.5f;
	for (int i = 0; i < 1299; i++) {
		if (i % 10 == 0)
			line++;
		terrain[i].setPosition((i%10)*80, 420-(line*60));
		terrain[i].setSize(sf::Vector2f(80, 60));
		if(levelData[i]==0)
			terrain[i].setFillColor(sf::Color::Black);
		else
			terrain[i].setFillColor(sf::Color::Red);

	}

	m_player.setRadius(20);
	m_player.setPosition(400, 500);
	m_player.setFillColor(sf::Color::Green);

	sf::Clock clock;
	sf::Time timeSinceLastUpdate = sf::Time::Zero;
	const float fps{ 60.0f };
	sf::Time timePerFrame = sf::seconds(1.0f / fps); // 60 fps
	while (m_window.isOpen())
	{
		processEvents(); // as many as possible
		timeSinceLastUpdate += clock.restart();
		while (timeSinceLastUpdate > timePerFrame)
		{
			timeSinceLastUpdate -= timePerFrame;
			processEvents(); // at least 60 fps
			update(timePerFrame); //60 fps
		}
		if (sf::Keyboard::isKeyPressed(sf::Keyboard::Right)) {
			m_player.setPosition(m_player.getPosition().x + 1, m_player.getPosition().y);
		}
		if (sf::Keyboard::isKeyPressed(sf::Keyboard::Left)) {
			m_player.setPosition(m_player.getPosition().x -1, m_player.getPosition().y);
		}

		int line = 0;

		for (int i = 0; i < 1299; i++) {
			if (i % 10 == 0) {
				line++;
			}
			terrain[i].setPosition((i % 10) * 80, 420 - (line * 60)-terrainDown);
			terrain[i].setSize(sf::Vector2f(80, 60));
			if (levelData[i] == 0)
				terrain[i].setFillColor(sf::Color::Black);
			else
				terrain[i].setFillColor(sf::Color::Red);

		}
		terrainDown -= terrainSpeed;
		for (int i = 0; i < 1299; i++) {
			if (m_player.getGlobalBounds().intersects(terrain[i].getGlobalBounds()) && terrain[i].getFillColor() == sf::Color::Red) {
				m_window.close();

			}
		}
		if (terrain[1299].getPosition().y > 700) {
			m_window.close();
		}

		render(); // as many as possible
	}
}
/// <summary>
/// handle user and system events/ input
/// get key presses/ mouse moves etc. from OS
/// and user :: Don't do game update here
/// </summary>
void Game::processEvents()
{
	sf::Event newEvent;
	while (m_window.pollEvent(newEvent))
	{
		if ( sf::Event::Closed == newEvent.type) // window message
		{
			m_exitGame = true;
		}
		if (sf::Event::KeyPressed == newEvent.type) //user pressed a key
		{
			processKeys(newEvent);
		}

	}
}


/// <summary>
/// deal with key presses from the user
/// </summary>
/// <param name="t_event">key press event</param>
void Game::processKeys(sf::Event t_event)
{
	if (sf::Keyboard::Escape == t_event.key.code)
	{
		m_exitGame = true;
	}
	
}

/// <summary>
/// Update the game world
/// </summary>
/// <param name="t_deltaTime">time interval per frame</param>
void Game::update(sf::Time t_deltaTime)
{
	if (m_exitGame)
	{
		m_window.close();
	}

}

/// <summary>
/// draw the frame and then switch buffers
/// </summary>
void Game::render()
{
	m_window.clear(sf::Color::Black);
	for (int i = 0; i < 1299; i++) {
		m_window.draw(terrain[i]);

	}
	m_window.draw(m_player);
	m_window.display();
}

